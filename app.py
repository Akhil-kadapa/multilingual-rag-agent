def generate_chat_pdf(messages, language):
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Helvetica", "B", 20)
    pdf.set_text_color(0, 119, 182)
    pdf.cell(0, 15, "Multilingual RAG System Agent", ln=True, align='C')
    pdf.set_font("Helvetica", "", 11)
    pdf.set_text_color(100, 100, 100)
    pdf.cell(0, 8, f"Chat History | Language: {language}", ln=True, align='C')
    pdf.ln(5)
    pdf.set_draw_color(0, 119, 182)
    pdf.line(10, pdf.get_y(), 200, pdf.get_y())
    pdf.ln(8)
    for i, message in enumerate(messages):
        if message['role'] == 'user':
            pdf.set_font("Helvetica", "B", 11)
            pdf.set_text_color(30, 58, 95)
            pdf.cell(0, 8, f"Question {(i//2)+1}:", ln=True)
            pdf.set_font("Helvetica", "", 10)
            pdf.set_text_color(0, 0, 0)
            pdf.multi_cell(0, 7, message['content'])
            pdf.ln(3)
        else:
            pdf.set_font("Helvetica", "B", 11)
            pdf.set_text_color(0, 119, 182)
            pdf.cell(0, 8, "Answer:", ln=True)
            pdf.set_font("Helvetica", "", 10)
            pdf.set_text_color(0, 0, 0)
            answer = message['content'].encode('latin-1', 'replace').decode('latin-1')
            pdf.multi_cell(0, 7, answer)
            pdf.ln(3)
            if 'evaluation' in message:
                score = message['evaluation']['faithfulness_score']
                pdf.set_font("Helvetica", "B", 9)
                pdf.set_text_color(100, 100, 100)
                pdf.cell(0, 7, f"Faithfulness Score: {score}/100 | Retrieval: {message['evaluation']['retrieval_quality']} | Problem: {message['evaluation']['problem_area']}", ln=True)
                pdf.set_font("Helvetica", "I", 9)
                diagnosis = message['evaluation']['diagnosis'].encode('latin-1', 'replace').decode('latin-1')
                pdf.cell(0, 7, f"Diagnosis: {diagnosis}", ln=True)
            if 'sources' in message:
                pdf.set_font("Helvetica", "B", 9)
                pdf.set_text_color(0, 119, 182)
                sources = list(set(message['sources']))
                pdf.cell(0, 7, f"Sources: {', '.join(sources)}", ln=True)
            pdf.ln(3)
            pdf.set_draw_color(200, 200, 200)
            pdf.line(10, pdf.get_y(), 200, pdf.get_y())
            pdf.ln(5)
    pdf.set_y(-20)
    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(150, 150, 150)
    pdf.cell(0, 10, "Generated by Multilingual RAG System Agent | Built by Akhil Kadapa", align='C')
    return bytes(pdf.output())


