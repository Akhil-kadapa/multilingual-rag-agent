    st.subheader("📁 Upload Documents")
    uploaded_files_sidebar = st.file_uploader(
        "Upload PDF or TXT files",
        type=["pdf", "txt"],
        accept_multiple_files=True,
        key="sidebar_upload"
    )

    if uploaded_files_sidebar:
        if st.button("🚀 Process Documents", use_container_width=True, type="primary"):
            progress_bar = st.progress(0)
            status_text = st.empty()

            status_text.text("Reading documents...")
            progress_bar.progress(20)

            temp_dir = tempfile.mkdtemp()
            for uploaded_file in uploaded_files_sidebar:
                file_path = os.path.join(temp_dir, uploaded_file.name)
                with open(file_path, "wb") as f:
                    f.write(uploaded_file.getbuffer())

            documents = load_documents(temp_dir)

            seen = set()
            unique_docs = []
            for doc in documents:
                if doc['filename'] not in seen:
                    seen.add(doc['filename'])
                    unique_docs.append(doc)
            documents = unique_docs

            if documents:
                status_text.text("Generating summaries...")
                progress_bar.progress(40)

                st.session_state.summaries = []
                for doc in documents:
                    summary = summarize_document(
                        doc['text'],
                        doc['filename'],
                        language
                    )
                    st.session_state.summaries.append({
                        "filename": doc['filename'],
                        "summary": summary
                    })

                status_text.text("Chunking documents...")
                progress_bar.progress(60)

                all_chunks = []
                for doc in documents:
                    chunks = chunk_text(doc['text'], doc['filename'])
                    all_chunks.extend(chunks)

                status_text.text("Creating embeddings...")
                progress_bar.progress(80)

                embeddings = create_embeddings(all_chunks)
                index, chunks = store_embeddings(embeddings, all_chunks)

                status_text.text("Ready!")
                progress_bar.progress(100)

                st.session_state.index = index
                st.session_state.chunks = chunks
                st.session_state.processed = True
                st.session_state.messages = []

                st.success(f"✅ {len(documents)} documents processed!")
                st.rerun()

